-- ============================================================
-- SECAO 1: CRIACAO DAS TABELAS
-- ============================================================

DROP TABLE IF EXISTS vendas_vendedor CASCADE;
DROP TABLE IF EXISTS itens_pedido    CASCADE;
DROP TABLE IF EXISTS pedidos         CASCADE;
DROP TABLE IF EXISTS produtos        CASCADE;
DROP TABLE IF EXISTS vendedores      CASCADE;
DROP TABLE IF EXISTS clientes        CASCADE;

CREATE TABLE clientes (
    cliente_id    SERIAL PRIMARY KEY,
    nome          VARCHAR(100) NOT NULL,
    cidade        VARCHAR(80),
    estado        CHAR(2),
    data_cadastro DATE,
    segmento      VARCHAR(40)
);

CREATE TABLE vendedores (
    vendedor_id    SERIAL PRIMARY KEY,
    nome_vendedor  VARCHAR(100) NOT NULL,
    regiao         VARCHAR(40)
);

CREATE TABLE produtos (
    produto_id    SERIAL PRIMARY KEY,
    nome_produto  VARCHAR(120) NOT NULL,
    categoria     VARCHAR(60),
    marca         VARCHAR(60),
    preco         NUMERIC(10,2)
);

CREATE TABLE pedidos (
    pedido_id    SERIAL PRIMARY KEY,
    cliente_id   INT REFERENCES clientes(cliente_id),
    data_pedido  DATE,
    valor_pedido NUMERIC(12,2),
    status       VARCHAR(20)
);

CREATE TABLE itens_pedido (
    item_id    SERIAL PRIMARY KEY,
    pedido_id  INT REFERENCES pedidos(pedido_id),
    produto_id INT REFERENCES produtos(produto_id),
    quantidade INT,
    valor_item NUMERIC(12,2)
);

CREATE TABLE vendas_vendedor (
    venda_id    SERIAL PRIMARY KEY,
    vendedor_id INT REFERENCES vendedores(vendedor_id),
    cliente_id  INT REFERENCES clientes(cliente_id),
    data_venda  DATE,
    valor_venda NUMERIC(12,2)
);


